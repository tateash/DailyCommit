package com.example.demo;
//Show the patterns left hand , right hand , down side star without using loop

public class ChThree {

	public static void main(String[] args) {

		/*
		 * * ** *** ****
		 */
        System.out.println("Left Hand star pattern\n");
		System.out.println("*");
		System.out.println("**");
		System.out.println("***");
		System.out.println("****");
		System.out.println("*****\n");

		

		System.out.println("Right Hand star pattern\n");
		System.out.println("    *");
		System.out.println("   **");
		System.out.println("  ***");
		System.out.println(" ****");
		System.out.println("*****\n");

		
		
		System.out.println("Down Hand star pattern\n");
		System.out.println("*****");
		System.out.println("****");
		System.out.println("***");
		System.out.println("**");
		System.out.println("*");

	}

}
